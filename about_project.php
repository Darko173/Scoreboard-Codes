      <p class="display-5">Batch of 2022</p>
<p class="lead">The team started to understand the project and decided not to use seven-segment displays as it was creating signal noise in the LEDs and the display was blurred. The PCBs were also not used and so they decided to go with DMDs. ACP sheet was also discarded due to low reliability and low strength. As an alternative, plywood was used. A waterproof, strong, and reliable design for the overall model was made. A new design for the frame was made which is reliable and mechanically advanced in holding acrylic sheets. A plywood board integrated with DMDs, and a back-cover of aluminium sheet were introduced which ensured that the Scoreboard remained waterproof. New boxes were introduced to put a battery and remaining electronic equipment. The stand was also modified in a way to hold a solar panel that can be rotated 360 degrees on a vertical axis, to fix the position of the solar panel, a solar tracker was also designed. A hybrid design was made which was interconnected but not interdependent, to power the Scoreboard from both AC (power supply) and DC (Solar Panel) with surge protection. Wheels were introduced in both upper and lower frame ensuring mobility and allowing the scoreboard to operate at two different heights. Meanwhile, the Software team implemented new Arduino codes for new improved architecture of the system. The Android application also required major changes in order to cope up with the architecture change. A new dedicated website was developed successfully to show real time data on it. It faciliated remote access of the match updates in real time.</p>
